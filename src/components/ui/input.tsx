import * as React from "react";
import { cn } from "@/lib/utils";

export interface InputProps
  extends React.InputHTMLAttributes<HTMLInputElement> {}

const Input = React.forwardRef<HTMLInputElement, InputProps>(
  ({ className, type, ...props }, ref) => {
    return (
      <input
        type={type}
        className={cn(
          "flex h-10 w-full min-h-[44px] rounded-md border border-white/10 bg-black/40 px-3 py-2 text-sm text-white shadow-sm transition-colors",
          "placeholder:text-white/40",
          "focus-visible:outline-none focus-visible:ring-1 focus-visible:ring-violet-500",
          "disabled:cursor-not-allowed disabled:opacity-50",
          className
        )}
        ref={ref}
        {...props}
      />
    );
  }
);
Input.displayName = "Input";

export { Input };
